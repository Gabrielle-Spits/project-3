        <h1 id="home-titels">Vormen ADHD</h1>
        <p>Er bestaan drie typen ADHD:<br>

            ADHD-I, vroeger ook wel ADD-type genoemd. Hierbij staat een aandachtstekort het meest op de voorgrond;<br>
            <br>
            ADHD-H, het overwegend hyperactieve en impulsieve type. Hier staan ernstige en aanhoudende impulsiviteit en hyperactiviteit op de voorgrond;<br>
            <br>
            ADHD-C, het gecombineerde type. Zowel problemen van het onoplettende als het hyperactieve type zijn aanwezig. Dit type ADHD komt het meeste voor en zal in deze tekst als uitgangspunt worden genomen.</p>
