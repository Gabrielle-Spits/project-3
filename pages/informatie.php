        <h2 id="h2-tussen-normaal">Wat betekent ADHD?</h2>
        <p>ADHD in een afkorting van Attention Deficit Hyperactivity Disorder oftwel in het Nederlands ook wel bekend als
            aandachtstekortstoornis met hyper activiteit</p>
    </div>
    <div class="col-4">
        <h2 id="h2-tussen-normaal">Symptomen ADHD</h2>
        <p>Volwassenen met ADHD ervaren vooral problemen met organisatie, planning en concentratie. Daarnaast
            vertonen mensen met ADHD vaak ook hyperactief en impulsief gedrag. ADHD kenmerken bij volwassenen zijn
            over het algemeen hetzelfde als bij kinderen met ADHD.</p>
    </div>
    <div class="col-4">
        <h2 id="h2-tussen-normaal">Hoe beleef je ADHD?</h2>
        <p>ADHD is een stoornis waarmee je altijd heel veel gedachtes in je hoofd hebt. Als voorbeeld waar mensen zonder
            ADHD 10 gedachtes hebben per minuut hebben wij
            100 dingen die tegelijk in hun hoofd.</p>
    </div>
</div>
<div class="row">
    <div class="col-6">
        <h2 id="h2-tussen-lang"> ADHD heeft 3 Vormen namelijk:</h2>
            <h3 id="h3-informatie-tussen">ADHD-I</h3>
            <p>ADHD-I, vroeger ook wel ADD-type genoemd. Hierbij staat een aandachtstekort het meest op de
            voorgrond</p>
            <h3 id="h3-informatie-tussen">ADHD-H</h3>
            <p>ADHD-H, het overwegend hyperactieve en impulsieve type. Hier staan ernstige en aanhoudende impulsiviteit
            en hyperactiviteit op de voorgrond.</p>
            <h3 id="h3-informatie-tussen">ADHD-C</h3>
            <p>
            ADHD-C, het gecombineerde type. Zowel problemen van het onoplettende als het hyperactieve type zijn
            aanwezig. Dit type ADHD komt het meeste voor en zal in deze tekst als uitgangspunt worden genomen.</p>
    </div>
    <div class="col-6">
        <h2 id="h2-tussen-kort">Achtergrond</h2>
        <h3 id="h3-informatie-tussen">Biologische oorzaak</h3>
        <p>
        De biologische oorzaak van ADHD ligt waarschijnlijk bij problemen met communicatie tussen hersengebieden. Signalen tussen hersencellen worden doorgegeven door chemische boodschappers. 
        </p>
        <h3 id="h3-informatie-tussen">Samenhangen</h3>
        <p>Dit gaat samen met afwijkende functie van de prefrontaalkwabben en een verminderde anatomische organisatie van de verbindingen tussen de prefrontaalkwabben en verschillende diepergelegen hersenkernen.</p>
        
