    <div class="col-6">
        <h2>Ken je iemand met ADHD?</h2>
    </div>
</div>

<div class="row">
    <div class="col-4">
        <p>ADHD is geen excuus. Net als iedereen is ook iemand met ADHD verantwoordelijk voor zijn eigen daden en gedrag. Als iemand met ADHD afspraken niet nakomt, of als hij/zij dingen doet of zegt die je niet leuk vindt, zeg dit dat ook.</p>
        <p>Bij mensen met ADHD is het vaak zwart/wit. Geen tint grijs ertussen. Wat de ander zegt is hoogstwaarschijnlijk niet zo bedoeld. De ander met ADHD kan dit heel anders opvatten.</p>
    </div>

    <div class="col-4">
        <p>Je kunt de ander wel behulpzaam zijn. Mocht de ander vaak afspraken vergeten, probeer een manier te vinden om de ander eraan te herinneren. De afspraak en een herinnering in de telefoon zetten, een app sturen zijn suggesties die je kunt proberen.</p>
        <p>Om last minute onaangename verrassingen te voorkomen kun je in sommige gevallen zelf zaken regelen en bewaren. Als je gaat reizen met het vliegtuig bijvoorbeeld. Jij bewaart de reservering en ticket, controleert samen of het paspoort nog geldig is.</p>
    </div>

    <div class="col-4">
        <p>Denk niet voor de ander. Je bent geen gedachtelezer. Stel actief vragen en luister. Probeer de ander niet te onderbreken. Houd oogcontact. Dit helpt dat de ander niet afdwaalt.</p>
        <p>Wees concreet. Vertel iets in korte zinnen, op duidelijke toon en kijk de ander in de ogen. Zo heb je contact.</p>
        <p>Maak heldere afspraken. Herhaal wat er is afgesproken en vraag na of de ander het begrepen heeft of dat er nog vragen zijn.</p>
    </div>
